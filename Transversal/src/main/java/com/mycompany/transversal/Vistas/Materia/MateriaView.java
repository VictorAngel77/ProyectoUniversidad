    private void checkEstado() {
        if (jcBaja.isSelected()) {
            JbBaja.setText("Alta");
            ListaMateriaBaja();
        } else {
            JbBaja.setText("Baja");
            ListaMateria();
        }
    }

    private boolean esNumero() {
        try {
            Integer.parseInt(jtBuscar.getText());
            return true;
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error debe ingresar un numero");
            checkEstado();
        }
        jtBuscar.setText("");
        return false;
    }

    private void ListaMateria() {
        List<Materia> listaMateria = matConn.listarMateriasCompleta();

        if (listaMateria.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay Materias en el sistema");
        } else {
            listaMateria.forEach(materia -> {
                modelo.addRow(new Object[]{
                    materia.getIdMateria(),
                    materia.getNombre(),
                    materia.getAño(),});
            });

        }

    }

    private void ListaMateriaBaja() {
        List<Materia> listaMateria = matConn.listarMateriasBaja();

        if (listaMateria.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay Materias de Baja en el sistema");
        } else {
            listaMateria.forEach(materia -> {
                modelo.addRow(new Object[]{
                    materia.getIdMateria(),
                    materia.getNombre(),
                    materia.getAño(),});
            });

        }

    }


